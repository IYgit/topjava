package ru.javawebinar.topjava.model;

public class AbstractEntity {
    protected Integer id;

    protected AbstractEntity(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public boolean isNew() {
        return this.id == null;
    }

}
